void dote(int k) {
    printf("\e[H\e[J\e[?7l");
    for (int i = 0; i < 16; i++) {
        printf("%*s", k, "");
        for (int j = 0; j < 16; j++) {
            int x = a[i][j];
            if (k % 2 == 1) {
                x = b[i][j];
            }
            printf("\e[48;5;%dm", color(x));
            printf("  ");
            printf("\e[0m");
        }
        printf("\n");
    }
    usleep(125000);
}

